#include<iostream>
using namespace std;
 
int main(){
    int num1,num2;
    cout<<"Enter the value of num1 :\n";  // This '<<' is called as Insertion operator 
    cin>>num1;  //This '>>'  is called as Extraction operator
    
    cout<<"Enter the value of num2 :\n";  // This '<<' is called as Insertion operator 
    cin>>num2;  //This '>>'  is called as Extraction operator
    
    cout<<"The Sum is : "<<num1+num2<<endl;
    return 0;
}












